# переменные должны бить понятны
# пишутся строчными буквами, слова в разделяются _
person_name = 'Max'

# в этом языке динамическая типизация в отличии от свифт и жс!
# тип назначается при создании переменной автоматически

# определить тип переменной можно так:
print(type(person_name))
# это <class 'str'>

age = 30
print(type(age))
# <class 'int'>

period = 3.2
print(type(period))
# <class 'float'>

is_good = True
print(type(is_good))
# <class 'bool'>

person = None
print(type(person))
# <class 'NoneType'>

